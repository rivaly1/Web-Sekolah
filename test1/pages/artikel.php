        <h1>Laptop</h1>
        <img class="imgr borderedbox" src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcR6Or6YveEl_DEPREbTu-YdUTyniA30iGeMtw&usqp=CAU" alt="">
        <p>Laptop atau komputer jinjing adalah komputer pribadi yang berukuran relatif kecil dan ringan sehingga sifatnya portabel.<br> Beratnya berkisar dari 1â€“6 kg, tergantung pada ukuran, bahan, dan spesifikasi laptop tersebut. Sumber daya laptop berasal dari baterai atau adaptor A/C yang dapat digunakan untuk mengisi ulang baterai dan menyalakan laptop itu sendiri.<br> Baterai laptop pada umumnya dapat bertahan kurang lebih 2 hingga 6 jam sebelum akhirnya habis. Habisnya baterai bergantung dari cara pemakaian, spesifikasi, dan ukuran baterai.<br>Laptop terkadang disebut juga dengan komputer notebook atau komputer pangku,<br>untuk ukuran yang lebih kecil disebut sebagai netbook.</p>
        <p>Sebagai komputer pribadi, laptop memiliki fungsi yang sama dengan komputer desktop<br>(desktop computers) pada umumnya. Komponen yang terdapat di dalamnya sama persis dengan komponen pada desktop, hanya saja ukurannya diperkecil, dijadikan lebih ringan, lebih tidak panas, dan lebih hemat daya.</p>
        <img class="imgl borderedbox" src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcSw5txgFPwS-IUGTq7OKDSJ9r6UFkWgPfhkPA&usqp=CAU" alt="">
        <p>Laptop kebanyakan menggunakan layar LCD (Liquid Crystal Display) berukuran 10 inci hingga 17 inci tergantung dari ukuran laptop itu sendiri. Selain itu, papan ketik yang terdapat pada laptop juga kadang-kadang dilengkapi dengan papan sentuh yang berfungsi sebagai "pengganti" tetikus. Papan ketik dan tetikus tambahan dapat dipasang melalui soket Universal Serial Bus maupun PS/2 jika tersedia.</p>
        <p>Berbeda dengan komputer desktop, laptop memiliki komponen pendukung yang didesain secara khusus untuk mengakomodasi sifat komputer jinjing yang portabel. Sifat utama yang dimiliki oleh komponen penyusun laptop adalah ukuran yang kecil, hemat konsumsi energi, dan efisien. Komputer jinjing biasanya harganya lebih mahal, tergantung dari merek dan spesifikasi komponen penyusunnya, walaupun demikian, harga komputer jinjing pun semakin mendekati desktop seiring dengan semakin tingginya tingkat permintaan konsumen.</p>
        <p>Namun saat ini dengan berbagai desain yang menarik dan kekinian, laptop menjadi lebih ringan dan tipis termasuk dari sisi teknologinya yang kian canggih.</p>
